public class OperacionesMatematicas {
    public static void main(String[] args) {
        
        double x = 2.1;
        double y = 3;

        System.out.println(Math.ceil(x));//redondeado hacia arriba
        System.out.println(Math.floor(x));//redondeado hacia abajo
        System.out.println(Math.pow(x, y));//potencia

    }
}
